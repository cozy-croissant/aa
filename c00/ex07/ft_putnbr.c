#include <unistd.h>

void	putchar(int n)
{
	char	c;

	if (n >= 0)
	{
		c = '0' + n;
		write(1, &c, 1);
	}
}

void	ft_42(int n)
{
	if (n >= 10)
	{
		ft_42(n / 10);
		ft_42(n % 10);
	}
	if (n < 10)
		putchar(n);
}

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (n < 0)
	{
		n *= -1;
		write(1, "-", 1);
		ft_42(n);
	}
	else
		ft_42(n);
}
