#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	hexa_print(char c)
{
	char	*hex;

	hex = "0123456789abcdef";
	ft_putchar('\\');
	ft_putchar(hex[(unsigned char)c / 16]);
	ft_putchar(hex[(unsigned char)c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (' ' > str[i] || str[i] > '~')
			hexa_print(str[i]);
		else
			ft_putchar(str[i]);
		i++;
	}
}
